                                                    Khwisero Sub-County on the other hand covers an area of 145.6 sq. km with a population of 102,635 inhabitants. It comprises 4 elective wards of Kisa North, Kisa East, Kisa West, Kisa Central. There are a total of 61 primary and 10 Secondary schools respectively.<br/><br/>
The economy of the two Sub-Counties is characterized by subsistence agriculture where crops cultivated are inclusive of maize, sorghum, cassava, beans, horticultural crops, sweet potatoes and to a lesser extent sugarcane for mill white sugar. Commercial farming activities in the Sub-County is minimal given the lack of industrial crops like coffee and tea but some farmers do keep dairy cattle for milk production. Manufacturing activities are also minimal since most of the manufactured products are sourced from Kisumu city, a distance of approximately 60 kilometres away. Lack of industries and minimal commercial farming has negatively affected the socio-economic lives of the residents of the Sub-Counties resulting in widespread poverty among the residents.

