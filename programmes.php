<br><i class="icon-right-dir" style="color: #2ab574;"></i>    High number of orphaned children as a result of the high prevalence of HIV/Aids in the Sub-Counties.
<br><i class="icon-right-dir" style="color: #2ab574;"></i>    Lack of gainful economic activities in the two communities


                                                                </p>
