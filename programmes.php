The general educational standard especially the number of pupils transitioning to Secondary Education from Khwisero and Gem Sub-Counties have been declining over the years. The situation has come about due to various factors:
<br><i class="icon-right-dir" style="color: #2ab574;"></i>    Neglect of this important service by community leaders and weakness of the national government educational administration system
<br><i class="icon-right-dir" style="color: #2ab574;"></i>    Limited budgetary provision from the national government for the educational programs since education sector from primary to University is a function of the National government
